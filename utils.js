
export const getParamsFromUrl = () => {
  const params = decodeURI(window.location.search.replace("?", ""))
  return params && JSON.parse(decodeURI(params))
}

export const cleanUrl = () => {
  history.pushState({}, "", `/`);
}

export const updateUrl = (params) => {
  history.pushState({}, "", `?${JSON.stringify(params)}`);
}

export const random = (minimum, maximum) => {
  return Math.floor(Math.random() * (maximum - minimum + 1) + minimum);
};

export const randomFrom = (array) => {
  const index = random(0, array.length);
  return array[index];
};

const getUniqueRandomColor = (palette) => {
  const color = randomFrom(COLORS);
  if (palette.includes(color)) return getUniqueRandomColor(palette);
  return color;
};

export const generatePalette = (size) => {
  const palette = [];
  for (let i = 0; i < size; i++) {
    const color = getUniqueRandomColor(palette);
    palette.push(color);
  }
  return palette;
};

export const generateFaviconFromSVG = (svg) => {
  var link = document.createElement("link")
  link.rel = "shortcut icon";
  link.href = `data:image/svg+xml;utf8,${svg.outerHTML}`;

  document.getElementsByTagName("head")[0].appendChild(link);
};

